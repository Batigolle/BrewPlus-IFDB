package jmash.report.model;

public class MineralSalts {
	
	private String calcium;
	private String magnesium;
	private String sodium;
	private String cloride;
	private String sulphate;
	private String pH;
	
	public String getCalcium() {
		return calcium;
	}
	public void setCalcium(String calcium) {
		this.calcium = calcium;
	}
	public String getMagnesium() {
		return magnesium;
	}
	public void setMagnesium(String magnesium) {
		this.magnesium = magnesium;
	}
	public String getSodium() {
		return sodium;
	}
	public void setSodium(String sodium) {
		this.sodium = sodium;
	}
	public String getCloride() {
		return cloride;
	}
	public void setCloride(String cloride) {
		this.cloride = cloride;
	}
	public String getSulphate() {
		return sulphate;
	}
	public void setSulphate(String sulphate) {
		this.sulphate = sulphate;
	}
	public String getpH() {
		return pH;
	}
	public void setpH(String pH) {
		this.pH = pH;
	}
}
